	/**
	 * 缩放图像（按高度和宽度缩放）
	 * 
	 * @param srcImageFile
	 *            源图像文件地址
	 * @param height
	 *            缩放后的高度
	 * @param width
	 *            缩放后的宽度
	 * @param flag
	 *            比例不对时是否需要补白：true为补白; false为不补白;
	 */
	public final static byte[] scale(byte[] img, int height, int width,
			boolean flag) {
		try {
			double ratio = 0.0; // 缩放比例
			InputStream inputStream = new ByteArrayInputStream(img);
			BufferedImage bi = ImageIO.read(inputStream);
			Image itemp = bi.getScaledInstance(width, height,
					Image.SCALE_SMOOTH);
			// 计算比例
			if ((bi.getHeight() > height) || (bi.getWidth() > width)) {
				if (bi.getHeight() > bi.getWidth()) {
					ratio = (new Integer(height)).doubleValue()
							/ bi.getHeight();
				} else {
					ratio = (new Integer(width)).doubleValue() / bi.getWidth();
				}
				AffineTransformOp op = new AffineTransformOp(
						AffineTransform.getScaleInstance(ratio, ratio), null);
				itemp = op.filter(bi, null);
			}
			if (flag) {// 补白
				BufferedImage image = new BufferedImage(width, height,
						BufferedImage.TYPE_INT_RGB);
				Graphics2D g = image.createGraphics();
				g.setColor(Color.white);
				g.fillRect(0, 0, width, height);
				if (width == itemp.getWidth(null))
					g.drawImage(itemp, 0, (height - itemp.getHeight(null)) / 2,
							itemp.getWidth(null), itemp.getHeight(null),
							Color.white, null);
				else
					g.drawImage(itemp, (width - itemp.getWidth(null)) / 2, 0,
							itemp.getWidth(null), itemp.getHeight(null),
							Color.white, null);
				g.dispose();
				itemp = image;
			}
			ByteArrayOutputStream outputstream = new ByteArrayOutputStream();
			ImageIO.write((BufferedImage) itemp, "JPEG", outputstream);
			return outputstream.toByteArray();
		} catch (IOException e) {
			e.printStackTrace();
		}
		return null;
	}

	/**
	 * 缩放图像（按比例缩放）
	 * 
	 * @param sourceImg
	 *            源图片
	 * @param output
	 *            缩放后输出的图片
	 * @param scale
	 *            缩放倍数 如2就是2倍
	 * @param flag
	 *            缩放选择:true 放大; false 缩小;
	 * @throws IOException
	 */
	public final static void scale(File sourceImg, File output, int scale,
			boolean flag) throws IOException {
		BufferedImage src = ImageIO.read(sourceImg); // 读入文件
		int width = src.getWidth(); // 得到源图宽
		int height = src.getHeight(); // 得到源图长
		if (flag) {// 放大
			width = width * scale;
			height = height * scale;
		} else {// 缩小
			width = width / scale;
			height = height / scale;
		}
		Image image = src.getScaledInstance(width, height, Image.SCALE_DEFAULT);
		BufferedImage tag = new BufferedImage(width, height,
				BufferedImage.TYPE_INT_RGB);
		Graphics g = tag.getGraphics();
		g.drawImage(image, 0, 0, null); // 绘制缩小后的图
		g.dispose();
		ImageIO.write(tag, "JPEG", output);// 输出到文件流
	}

	/**
	 * 缩放图像（按高度和宽度缩放） 不按比例缩放，图片会失真
	 * 
	 * @param sourceImg
	 *            源图像文件
	 * @param output
	 *            缩放后的图像
	 * @param height
	 *            缩放后的高度
	 * @param width
	 *            缩放后的宽度
	 * @param flag
	 *            比例不对时是否需要补白：true为补白; false为不补白;
	 */
	public final static void scale(File sourceImg, File output, int height,
			int width, boolean flag) {
		try {
			double ratio = 0.0; // 缩放比例
			BufferedImage bi = ImageIO.read(sourceImg);
			Image itemp = bi.getScaledInstance(width, height,
					Image.SCALE_SMOOTH);
			// 计算比例
			if ((bi.getHeight() > height) || (bi.getWidth() > width)) {
				if (bi.getHeight() > bi.getWidth()) {
					ratio = (new Integer(height)).doubleValue()
							/ bi.getHeight();
				} else {
					ratio = (new Integer(width)).doubleValue() / bi.getWidth();
				}
				AffineTransformOp op = new AffineTransformOp(
						AffineTransform.getScaleInstance(ratio, ratio), null);
				itemp = op.filter(bi, null);
			}
			if (flag) {// 补白
				BufferedImage image = new BufferedImage(width, height,
						BufferedImage.TYPE_INT_RGB);
				Graphics2D g = image.createGraphics();
				g.setColor(Color.white);
				g.fillRect(0, 0, width, height);
				if (width == itemp.getWidth(null))
					g.drawImage(itemp, 0, (height - itemp.getHeight(null)) / 2,
							itemp.getWidth(null), itemp.getHeight(null),
							Color.white, null);
				else
					g.drawImage(itemp, (width - itemp.getWidth(null)) / 2, 0,
							itemp.getWidth(null), itemp.getHeight(null),
							Color.white, null);
				g.dispose();
				itemp = image;
			}
			ImageIO.write((BufferedImage) itemp, "JPEG", output);
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

	/**
	 * 根据指定宽度压缩图片
	 * 
	 * @author fbf
	 * 
	 * @param sourceImg
	 * @param output
	 * @param width
	 */
	public final static void scaleByWidth(File sourceImg, File output, int width) {
		try {
			BufferedImage bi = ImageIO.read(sourceImg);
			int imageWidth = bi.getWidth();
			float sacle = (width * 1.0f) / (imageWidth * 1.0f);
			int height = (int) (bi.getHeight() * sacle);
			Image itemp = bi.getScaledInstance(width, height,
					Image.SCALE_SMOOTH);
			// 宽度缩放比
			double widthRatio = (new Integer(width)).doubleValue()
					/ bi.getWidth();
			// 高度缩放比
			double heightRatio = (new Integer(height)).doubleValue()
					/ bi.getHeight();
			AffineTransformOp op = new AffineTransformOp(
					AffineTransform.getScaleInstance(widthRatio, heightRatio),
					null);
			itemp = op.filter(bi, null);
			ImageIO.write((BufferedImage) itemp, "JPEG", output);
		} catch (IOException e) {
			throw new RuntimeException(e);
		}
	}

	/**
	 * 图片缩放
	 */

	public static void zoomImage(String src, String dest, int w, int h)
			throws IOException {
		double wr = 0, hr = 0;
		File srcFile = new File(src);
		File destFile = new File(dest);
		BufferedImage bufImg = ImageIO.read(srcFile);
		Image Itemp = bufImg.getScaledInstance(w, h, Image.SCALE_SMOOTH);
		wr = w * 1.0 / bufImg.getWidth();
		hr = h * 1.0 / bufImg.getHeight();
		AffineTransformOp ato = new AffineTransformOp(
				AffineTransform.getScaleInstance(wr, hr), null);
		Itemp = ato.filter(bufImg, null);
		ImageIO.write((BufferedImage) Itemp,
				dest.substring(dest.lastIndexOf(".") + 1), destFile);
	}
